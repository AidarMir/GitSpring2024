package CodingBat.Warmap1;

public class or35 {
    public boolean or35(int n) {

        if (n % 3 == 0 || n % 5 == 0) {
            return true;
        } return false;

    }

// Возвращайте true, если заданное неотрицательное число кратно 3 или кратно 5.
// Используйте оператор % «mod» — см. «Введение в мод».
}
