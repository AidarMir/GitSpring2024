package CodingBat.Warmap1;

public class in3050 {
    public boolean in3050(int a, int b) {

        return  ((a >= 30 && a <= 40) && (b >= 30 && b <= 40)) ||
                ((a >= 40 && a <= 50) && (b >= 40 && b <= 50));
    }
// Учитывая два целочисленных значения, верните true, если они оба находятся
// в диапазоне 30–40 включительно или оба находятся в диапазоне 40–50 включительно.
}
